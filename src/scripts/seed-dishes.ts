async function seedDishes() {
  console.log("🌱 fp_dish 시드 시작...");

  const { error: dishError } = await supabase
    .from("fp_dish")
    .upsert(DISHES, { onConflict: "dish_id" });

  if (dishError) {
    console.error("❌ fp_dish 삽입 오류:", dishError.message);
    process.exit(1);
  }
  console.log(`✅ fp_dish ${DISHES.length}개 완료`);

  const { error: ingredError } = await supabase
    .from("fp_dish_ingredient")
    .upsert(INGREDIENTS, { onConflict: "ingredient_id" });

  if (ingredError) {
    console.error("❌ fp_dish_ingredient 삽입 오류:", ingredError.message);
    process.exit(1);
  }
  console.log(`✅ fp_dish_ingredient ${INGREDIENTS.length}개 완료`);
  console.log("🎉 음식 마스터 시드 완료!");
}

seedDishes().catch(console.error);
