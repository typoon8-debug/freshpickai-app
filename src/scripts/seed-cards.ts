async function seedCards() {
  console.log("🌱 fp_menu_card 시드 시작...");

  const { error: cardError } = await supabase
    .from("fp_menu_card")
    .upsert(CARDS, { onConflict: "card_id" });

  if (cardError) {
    console.error("❌ fp_menu_card 삽입 오류:", cardError.message);
    process.exit(1);
  }
  console.log(`✅ fp_menu_card ${CARDS.length}개 완료`);

  const { error: cdError } = await supabase
    .from("fp_card_dish")
    .upsert(CARD_DISHES, { onConflict: "card_dish_id" });

  if (cdError) {
    console.error("❌ fp_card_dish 삽입 오류:", cdError.message);
    process.exit(1);
  }
  console.log(`✅ fp_card_dish ${CARD_DISHES.length}개 완료`);
  console.log("🎉 카드 시드 완료! (10종 × 3개 = 30개)");
}

seedCards().catch(console.error);
